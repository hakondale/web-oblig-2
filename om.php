                <h1>om denne siden</h1>
                <p> Da jeg skulle planlegge nettstedet var mitt fokus å fremheve artiklene. Siden det var to andre seksjoner som skulle inneholde mindre mengder med info tenkte jeg det ville passe seg bra som en meny på høyre siden. Grunnen til at jeg plasserte denne menyen på høyre siden og ikke venstre er fordi blikket trekkes generelt mer mot venstre delen av skjermen. Jeg bestemte meg for å dele skjermen opp i fire horisontale fraksjoner slik at jeg kunne få plass til sidemenyen, og tre artikler for hver rad. Med tre artikler per rad er det akkurat nok plass til en overskrift, ingress og en «les mer» knapp. Jeg lot denne knappen dekke hele «artikkel-menyen» sin bredde, men samtidig ha en lav høyde. Dette ser bra ut, og det gjør at knappen er lett å se, og lett å trykke på. Sidemenyen strekker seg forbi headeren så det skulle bli mer plass på sidemenyen. Headeren er ikke like viktig, og den trenger ikke å ta hele raden.
                </p>
                <p> På artikkelsiden er oppsettet ganske likt. Siden det ikke skal være fryktelig mye tekst i disse artiklene lot jeg artikkel-delen oppta 50% (eller to fraksjoner) av siden, og en hel fraksjon til å ha bilde. Dette gjør at teksten ikke strekker seg over så alt for lange linjer, noe som kan øke lesbarheten, og det ser pent ut med et stort bilde. Litt må man jo også lide for skjønnheten.
                </p>
                <p> Designet på siden min er ikke inspirert av en spesiell side, men heller en stil som ofte kalles for «vaporwave». Det skal etterligne og overdrive stiler fra 80 og 90-tallet. Jeg er ganske fascinert av denne stilen og har dradd inspirasjon fra klassiske elementer som er tilknyttet vaporwave. Dette inkluderer blant annet palmer, «data»-fonter, og rosa/rød/blå/lilla farger. 
                </p>  
